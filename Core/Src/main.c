char* generateSymbols(int length) {
    char* symbols = malloc(sizeof(char) * (length + 1));  // allocate space for the string and null terminator
    int i;
    srand(time(NULL));
    for (i = 0; i < length; i++) {
        symbols[i] = rand() % 94 + 33;  // keeps the range of symbols between ASCII 33 ('!') and ASCII 126 ('~')
    }
    symbols[length] = '\0';  // add null terminator to the end of the string
    return symbols;
}
